# Biblioteca de funciones para trabajar con cadenas


def longitud(cadena: str) -> int:
    l = length(cadena)

    return l


def invertir(cadena: str) -> str:
    """
    Invierte una cadena.

    :param cadena: Cadena a evaluar.

    :return: Cadena invertida.
    """

    return reverse(cadena)        

